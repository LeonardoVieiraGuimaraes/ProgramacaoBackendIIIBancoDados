package AtributosIndicadoresVariaveis;

public class VariaveisTiposDados {
    public static void main(String[] args) {
        // Tipos de dados primitivos
        int myInt = 10; // Inteiro
        double myDouble = 20.0; // Número de ponto flutuante
        char myChar = 'a'; // Caractere (Literal)
        boolean myBoolean = true; // Booleano (Lógico)
        byte myByte = 30; // Byte
        short myShort = 40; // Short (Curto)
        long myLong = 50L; // Long (Longo)
        float myFloat = 60.0f; // Float (Real)

        // Exibindo os valores das variáveis primitivas
        System.out.println("Int (Inteiro): " + myInt);
        System.out.println("Double (Real): " + myDouble);
        System.out.println("Char (Literal): " + myChar);
        System.out.println("Boolean (Lógico): " + myBoolean);
        System.out.println("Byte (Byte): " + myByte);
        System.out.println("Short (Curto): " + myShort);
        System.out.println("Long (Longo): " + myLong);
        System.out.println("Float (Real): " + myFloat);
    }
}