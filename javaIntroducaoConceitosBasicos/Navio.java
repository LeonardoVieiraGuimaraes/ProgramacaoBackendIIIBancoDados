package conceitosBasicos;

public class Navio {
    private String cor;
    private String combustivel;
    private int qtePassageiros;
    private int qtdTripulantes;

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public String getCombustivel() {
        return combustivel;
    }

    public void setCombustivel(String combustivel) {
        this.combustivel = combustivel;
    }

    public int getQtePassageiros() {
        return qtePassageiros;
    }

    public void setQtePassageiros(int qtePassageiros) {
        this.qtePassageiros = qtePassageiros;
    }

    public int getQtdTripulantes() {
        return qtdTripulantes;
    }

    public void setQtdTripulantes(int qtdTripulantes) {
        this.qtdTripulantes = qtdTripulantes;
    }

}
