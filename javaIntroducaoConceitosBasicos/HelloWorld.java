package javaIntroducaoConceitosBasicos;

/**
 * A classe HelloWorld contém o método main - o ponto de entrada do programa.
 */
public class HelloWorld {

    /**
     * Este é o método main. Ele imprime "Olá, Mundo!" na saída padrão.
     *
     * @param args Argumentos de linha de comando. Este programa não usa argumentos
     *             de linha de comando.
     */
    public static void main(String[] args) {
        // Imprime "Olá, Mundo!" na saída padrão
        System.out.println("Olá, Mundo!");
    }
}